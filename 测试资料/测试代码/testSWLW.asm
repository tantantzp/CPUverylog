	NOP

FIBWIRTEINI:
	LI R1 0x01		;R1 : Fib[0]
	LI R2 0x01		;R2 : Fib[1]
	LI R3 0x84		;R3 : MEM_ADDR
	SLL R3 R3 0x00
	LI R4 0x05		;R4 : REPEAT TIMES

LOOP:
	SW R3 R1 0x00
	SW R3 R2 0x01
	ADDU R1 R2 R1
	ADDU R1 R2 R2
	ADDIU R3 0x02
	ADDIU R4 0xFF
	BNEZ R4 LOOP
	NOP


FIBREADINI:
	LI R3 0x84		;R3 : MEM_ADDR
	SLL R3 R3 0x00
	LI R4 0x0a		;R4 : REPEAT TIMES
	LI R2 0xBF		;R2 : Show via 0xBF00
	SLL R2 R2 0x00
	B TESTW
	NOP

WRITEFIB:	
	NOP
    LI R1 0x00
    MTSP R1
	LW R3 R5 0x00		;R5 : LOAD_NUM
    NOP  
	SW R2 R5 0x00		;SHOW
	ADDIU R3 0x01
	ADDIU R4 0xFF
	NOP
    B TESTW
    NOP

TESTLOOP:
	NOP
    LI R1 0xFF
    MTSP R1
TESTW:	
	NOP	 		
	LI R6 0x00BF		;R6 : BF01 
	SLL R6 R6 0x0000 
	ADDIU R6 0x0001 
	LW R6 R0 0x0000 
	LI R6 0x0001 
	AND R0 R6 
	BEQZ R0 TESTLOOP
	NOP
	BNEZ R4 WRITEFIB
	NOP

	NOP
